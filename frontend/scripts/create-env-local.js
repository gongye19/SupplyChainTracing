#!/usr/bin/env node
// 创建空的 .env.local 文件，避免 Vite 构建时读取失败
import { writeFileSync } from 'fs';

try {
  writeFileSync('.env.local', '# Auto-generated by build script\n', 'utf8');
  console.log('Created .env.local file');
} catch (error) {
  // 如果无法创建文件（如权限问题），静默忽略
  console.warn('Could not create .env.local file (this is OK in some environments):', error.message);
}

